<h3>Registration for Families New to GLSA</h3>

<p>Registration for families new to GLSA begins on Monday, April 8th 2013.  If it is after this date and you are interested in registering for GLSA please call or email the GLSA office and check for availability of space. If there is space available please <a href="#process">complete the registration process described below</a>. While we will not process new family enrollment packets until open enrollment starts on April 8th new families can print them off the website and get them to GLSA as soon as they are available on Monday, March 18th.  We will number and process new family registration packets in the order we have received them. <em> Please note that GLSA cannot hold spaces until we have received all necessary forms which are completed with full address and all requested information filled out and we have received the registration and deposit fees. </em>When we have received your paperwork and fees and processed your paperwork we will contact you and confirm your enrollment.</p>

<p>If your receive notification that our program is full you will have the option to be put on the waiting list. In this case your registration and deposit check will not be cashed until you receive a placement with us. You will be contacted as soon as there is space available for your child on the day(s) needed.</p>

<h3>Registration for Currently Enrolled Families for the following school Year</h3>

<p>Currently enrolled families may begin registering for the 2013-2014 school year and the summer of 2013 on Monday, March 18th.  Currently enrolled families have a three week priority enrollment period during which your child&apos;s school year schedule at the time of registration is guaranteed to them.  This priority registration ends on Monday, April 8th at which time we start &#34;open enrollment&#34; for new families or families who would like to expand their current schedule next year.  Please <a href="#process">complete the registration process described below</a>, or request a registration packet from the office.</p>

